/**
 * Get a random selection of prompts
 * @param {number} count - Number of random prompts to return (default: 8)
 * @param {string} excludeCategory - Optional category to exclude
 * @returns {Array} Array of random prompts
 */
export const getRandomPrompts = (count = 8, excludeCategory = null) => {
  let availablePrompts = [...promptDatabase];

  // Filter out excluded category if specified
  if (excludeCategory) {
    availablePrompts = availablePrompts.filter(p => p.category !== excludeCategory);
  }

  // Shuffle array using Fisher-Yates algorithm
  for (let i = availablePrompts.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [availablePrompts[i], availablePrompts[j]] = [availablePrompts[j], availablePrompts[i]];
  }

  // Return first 'count' items
  return availablePrompts.slice(0, count);
};

/**
 * Get prompts by category
 * @param {string} category - Category name
 * @returns {Array} Array of prompts in that category
 */
export const getPromptsByCategory = (category) => {
  return promptDatabase.filter(p => p.category === category);
};

/**
 * Get all unique categories
 * @returns {Array} Array of unique category names
 */
export const getAllCategories = () => {
  return [...new Set(promptDatabase.map(p => p.category))];
};

/**
 * Get a random single prompt
 * @returns {Object} Random prompt object
 */
export const getRandomPrompt = () => {
  return promptDatabase[Math.floor(Math.random() * promptDatabase.length)];
};
